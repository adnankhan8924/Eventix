fun loadApiKeyProperties(): Properties {
    val properties = Properties()
    val propertiesFile = file(rootDir.path + "/keys.properties")
    if (propertiesFile.exists()) {
        properties.load(propertiesFile.inputStream())
    }
    return properties
}